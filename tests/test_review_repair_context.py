from __future__ import annotations

import tomllib
import unittest
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]


def read(path: str) -> str:
    return (ROOT / path).read_text(encoding="utf-8")


class ReviewRepairContextTests(unittest.TestCase):
    def test_review_template_blocks_fail_until_later_pass(self):
        template = read("templates/TEMPLATE.ticket-review.md")
        prompt = read("prompts/independent-reviewer.md")
        combined = f"{template}\n{prompt}"
        self.assertIn("actual diff", combined)
        self.assertIn("tests", combined)
        self.assertIn("evidence", combined)
        self.assertIn("ticket scope", combined)
        self.assertIn("spec", combined)
        self.assertIn("later independent review", combined)
        self.assertIn("PASS", combined)
        self.assertIn("FAIL", combined)
        self.assertIn("Do not mark the ticket done after `FAIL`", template)

    def test_fail_findings_are_bounded_and_reproducible(self):
        template = read("templates/TEMPLATE.ticket-review.md")
        required = [
            "Finding id",
            "Severity",
            "File or path",
            "Expected",
            "Actual",
            "Failed criterion",
            "Reproduction or evidence",
            "Required repair",
        ]
        for item in required:
            self.assertIn(item, template)

    def test_repair_cap_and_no_weakening_are_encoded(self):
        template = read("templates/TEMPLATE.ticket-review.md")
        checklist = read("checklists/review-repair-context.md")
        fixture = read(
            "docs/implementation/workflow-v0.5/tickets/"
            "TKT-2026-06-22-aew-v05-08-review-repair-blocker-context/"
            "REVIEW_REPAIR_FIXTURE.md"
        )
        combined = f"{template}\n{checklist}\n{fixture}"
        self.assertIn("Maximum repair attempts: `3`", combined)
        self.assertIn("materially different", combined)
        self.assertIn("Acceptance criteria unchanged", combined)
        self.assertIn("Tests weakened", combined)
        self.assertIn("blocker capsule", combined)

    def test_blocker_policy_matrix_preapproved_and_user_required(self):
        prompt = read("prompts/blocker-resolver.md")
        matrix = read(
            "docs/implementation/workflow-v0.5/tickets/"
            "TKT-2026-06-22-aew-v05-08-review-repair-blocker-context/"
            "BLOCKER_DECISION_MATRIX.md"
        )
        combined = f"{prompt}\n{matrix}"
        preapproved = [
            "focused in-scope repair",
            "dependency install",
            "reversible local or test-only migration",
            "cleanup of artifacts generated by the active run",
            "scoped commit and push",
        ]
        user_required = [
            "secrets",
            ".env*",
            "production",
            "deploy",
            "release",
            "publish",
            "external side effects",
            "force push",
            "remote history rewrite",
            "irreversible migrations",
            "product, policy, scope, or acceptance decisions",
        ]
        for item in preapproved:
            self.assertIn(item, combined)
        for item in user_required:
            self.assertIn(item, combined)
        for item in ["classification", "action", "validation", "rollback", "user_question"]:
            self.assertIn(item, prompt)

    def test_blocker_capsule_excludes_full_transcripts_and_raw_logs(self):
        capsule = read("templates/TEMPLATE.blocker-capsule.md")
        prompt = read("prompts/blocker-resolver.md")
        combined = f"{capsule}\n{prompt}"
        self.assertIn("minimal facts capsule", combined)
        self.assertIn("not a raw transcript", combined)
        self.assertIn("full logs", combined)
        self.assertIn("secrets", combined)
        self.assertIn(".env*", combined)
        self.assertIn("unrelated context", combined)

    def test_context_capsule_allowlist_and_redaction(self):
        curator = read("prompts/context-curator.md")
        ledger = read("templates/TEMPLATE.context-ledger.md")
        handoff = read(
            "docs/implementation/workflow-v0.5/tickets/"
            "TKT-2026-06-22-aew-v05-08-review-repair-blocker-context/"
            "COMPACT_HANDOFF_EXAMPLE.md"
        )
        combined = f"{curator}\n{ledger}\n{handoff}"
        allowlist = [
            "ticket id",
            "changed workflow contracts",
            "accepted decisions",
            "proof commands",
            "repair rounds",
            "residual risks",
            "next-agent context",
        ]
        redactions = [
            "raw transcripts",
            "raw logs",
            "secrets",
            ".env*",
            ".git/**",
            "node_modules/**",
            "dist/**",
            "build/**",
            "coverage/**",
            "**/secrets/**",
            "unrelated local paths",
        ]
        for item in allowlist:
            self.assertIn(item, combined)
        for item in redactions:
            self.assertIn(item, combined)
        self.assertIn("after an independent review returns `PASS`", curator)

    def test_autonomous_docs_reference_review_repair_context_contract(self):
        docs = read("docs/autonomous_execution.md")
        agents = read("templates/AGENTS.md.template")
        closeout = read("checklists/closeout.md")
        combined = f"{docs}\n{agents}\n{closeout}"
        self.assertIn("three materially different repair attempts", combined)
        self.assertIn("minimal blocker capsule", combined)
        self.assertIn("Context curation runs only after independent review `PASS`", combined)
        self.assertIn("cannot be marked done after a review `FAIL`", combined)

    def test_reviewer_and_blocker_native_profiles_are_read_only(self):
        for profile in [
            ".codex/agents/workflow-independent-reviewer.toml",
            ".codex/agents/workflow-blocker-resolver.toml",
        ]:
            data = tomllib.loads(read(profile))
            self.assertEqual("read-only", data.get("sandbox_mode"), profile)
            instructions = data.get("developer_instructions", "")
            self.assertIn("Do not edit", instructions, profile)


if __name__ == "__main__":
    unittest.main()
